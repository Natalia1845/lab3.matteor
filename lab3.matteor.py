import math

# Функція корисності
def utility(x):
    return math.log(x + 30)

# Лотереї (x1, p1, x2, p2)
lotteries = [
    (-20, 0.5, -10, 0.5),  # L(-20; 0.5; -10)
    (-25, 0.5, -15, 0.5),  # L(-25; 0.5; -15)
    (-29, 0.5, -19, 0.5)   # L(-29; 0.5; -19)
]

# Функція для обчислення сподіваного виграшу
def expected_value(x1, p1, x2, p2):
    return p1 * x1 + p2 * x2

# Функція для обчислення детермінованого еквіваленту
def certainty_equivalent(x1, p1, x2, p2):
    # Розв'язуємо рівняння U(CE) = p1 * U(x1) + p2 * U(x2)
    # Універсальне рішення для CE:
    return math.exp(p1 * utility(x1) + p2 * utility(x2)) - 30

# Функція для обчислення премії за ризик
def risk_premium(E, CE):
    return E - CE

# Обчислюємо для кожної лотереї
for i, (x1, p1, x2, p2) in enumerate(lotteries):
    E = expected_value(x1, p1, x2, p2)  # Сподіваний виграш
    CE = certainty_equivalent(x1, p1, x2, p2)  # Детермінований еквівалент
    RP = risk_premium(E, CE)  # Премія за ризик

    # Виведення результатів
    print(f"Лотерея {i+1}:")
    print(f"  Сподіваний виграш: {E}")
    print(f"  Детермінований еквівалент: {CE}")
    print(f"  Премія за ризик: {RP}")
    print()

